use std::sync::{Arc, Mutex};
use std::thread;

fn main() {
    let counter = Arc::new(Mutex::new(0));
    let mut handles = vec![];

    for _ in 0..10 {
        let arc_counter = Arc::clone(&counter); // borrow counter before executing threads
        let handle = thread::spawn(move || {    // arc_counter is owned copy of Arc, move it
            let mut num = arc_counter.lock().unwrap();
            *num += 1;
        });
        handles.push(handle);
    }

    for handle in handles {
        handle.join().unwrap();
    }

    println!("Result: {}", *counter.lock().unwrap());
}

